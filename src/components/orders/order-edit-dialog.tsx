'use client'

import React, { useState, useMemo, useEffect } from 'react'
import { Dialog, DialogContent, DialogHeader, DialogTitle, type DialogLevel } from '@/components/ui/dialog'
import { Button } from '@/components/ui/button'
import { Input } from '@/components/ui/input'
import { Combobox } from '@/components/ui/combobox'
import { useEmployeesSlim } from '@/data'
import { updateOrder } from '@/data'
import type { Order, Employee } from '@/stores/types'
import type { SyncableEntity } from '@/types'
import { logger } from '@/lib/utils/logger'

// 型別守衛：檢查 Employee 是否包含同步欄位
type EmployeeWithSync = Employee & Partial<SyncableEntity>

interface OrderEditDialogProps {
  open: boolean
  onOpenChange: (open: boolean) => void
  order: Order | null
  level?: DialogLevel
}

export function OrderEditDialog({
  open,
  onOpenChange,
  order,
  level = 2,
}: OrderEditDialogProps) {
  const { items: employees } = useEmployeesSlim()
  const [isSubmitting, setIsSubmitting] = useState(false)
  const [formData, setFormData] = useState({
    contact_person: '',
    sales_person: '',
    assistant: '',
  })

  // 當 order 變更時重設表單
  useEffect(() => {
    if (order) {
      setFormData({
        contact_person: order.contact_person || '',
        sales_person: order.sales_person || '',
        assistant: order.assistant || '',
      })
    }
  }, [order])

  // 排序函數：按員工編號排序
  const sortByEmployeeNumber = (a: Employee, b: Employee) => {
    const numA = a.employee_number || ''
    const numB = b.employee_number || ''
    return numA.localeCompare(numB, 'en', { numeric: true })
  }

  // 篩選業務人員（roles 包含 'sales'）
  const salesPersons = useMemo(() => {
    const activeEmployees = employees.filter(emp => {
      const empWithSync = emp as EmployeeWithSync
      const notDeleted = !empWithSync._deleted
      const isActive = emp.status === 'active'
      // 排除機器人
      const notBot = emp.employee_number !== 'BOT001' && emp.id !== '00000000-0000-0000-0000-000000000001'
      return notDeleted && isActive && notBot
    })

    const salesOnly = activeEmployees.filter(emp => emp.roles?.includes('sales'))

    // 如果有標記業務的就只顯示業務，沒有就顯示所有人
    const result = salesOnly.length > 0 ? salesOnly : activeEmployees
    return result.sort(sortByEmployeeNumber)
  }, [employees])

  // 篩選助理（roles 包含 'assistant'）
  const assistants = useMemo(() => {
    const activeEmployees = employees.filter(emp => {
      const empWithSync = emp as EmployeeWithSync
      const notDeleted = !empWithSync._deleted
      const isActive = emp.status === 'active'
      // 排除機器人
      const notBot = emp.employee_number !== 'BOT001' && emp.id !== '00000000-0000-0000-0000-000000000001'
      return notDeleted && isActive && notBot
    })

    const assistantsOnly = activeEmployees.filter(emp => emp.roles?.includes('assistant'))

    // 如果有標記助理的就只顯示助理，沒有就顯示所有人
    const result = assistantsOnly.length > 0 ? assistantsOnly : activeEmployees
    return result.sort(sortByEmployeeNumber)
  }, [employees])

  const handleSubmit = async (e: React.FormEvent) => {
    e.preventDefault()
    if (!order) return

    setIsSubmitting(true)
    try {
      await updateOrder(order.id, {
        contact_person: formData.contact_person,
        sales_person: formData.sales_person || null,
        assistant: formData.assistant || null,
      })
      onOpenChange(false)
    } catch (error) {
      logger.error('[OrderEditDialog] 更新訂單失敗:', error)
      alert(error instanceof Error ? error.message : '更新訂單失敗，請稍後再試')
    } finally {
      setIsSubmitting(false)
    }
  }

  return (
    <Dialog open={open} onOpenChange={onOpenChange}>
      <DialogContent level={level} className="max-w-lg">
        <DialogHeader>
          <DialogTitle>
            編輯訂單 {order?.order_number}
          </DialogTitle>
        </DialogHeader>

        <form onSubmit={handleSubmit} className="space-y-4">
          {/* 訂單資訊（唯讀） */}
          <div className="p-3 bg-morandi-container/30 rounded-lg space-y-1">
            <div className="text-xs text-morandi-secondary">
              旅遊團：<span className="font-medium text-morandi-primary">{order?.tour_name}</span>
            </div>
          </div>

          {/* 聯絡人 */}
          <div>
            <label className="text-sm font-medium text-morandi-primary">聯絡人</label>
            <Input
              value={formData.contact_person}
              onChange={e => setFormData(prev => ({ ...prev, contact_person: e.target.value }))}
              placeholder="輸入聯絡人姓名"
              className="mt-1"
              required
            />
          </div>

          {/* 業務和助理 */}
          <div className="grid grid-cols-2 gap-4">
            <div>
              <label className="text-sm font-medium text-morandi-primary">
                業務
                {formData.contact_person?.trim() && (
                  <span className="text-morandi-red ml-1">*</span>
                )}
              </label>
              <Combobox
                options={salesPersons.map(emp => ({
                  value: emp.display_name || emp.english_name,
                  label: `${emp.display_name || emp.english_name} (${emp.employee_number})`,
                }))}
                value={formData.sales_person}
                onChange={value => setFormData(prev => ({ ...prev, sales_person: value }))}
                placeholder="選擇業務人員..."
                emptyMessage="找不到業務人員"
                showSearchIcon={true}
                showClearButton={true}
                className="mt-1"
                disablePortal={true}
              />
            </div>
            <div>
              <label className="text-sm font-medium text-morandi-primary">助理</label>
              <Combobox
                options={assistants.map(emp => ({
                  value: emp.display_name || emp.english_name,
                  label: `${emp.display_name || emp.english_name} (${emp.employee_number})`,
                }))}
                value={formData.assistant}
                onChange={value => setFormData(prev => ({ ...prev, assistant: value }))}
                placeholder="選擇助理..."
                emptyMessage="找不到助理"
                showSearchIcon={true}
                showClearButton={true}
                className="mt-1"
                disablePortal={true}
              />
            </div>
          </div>

          {/* 按鈕 */}
          <div className="flex justify-end space-x-2 pt-2">
            <Button
              type="button"
              variant="outline"
              onClick={() => onOpenChange(false)}
              disabled={isSubmitting}
            >
              取消
            </Button>
            <Button
              type="submit"
              disabled={!formData.contact_person || isSubmitting}
              className="bg-morandi-gold hover:bg-morandi-gold-hover text-white"
            >
              {isSubmitting ? '儲存中...' : '儲存'}
            </Button>
          </div>
        </form>
      </DialogContent>
    </Dialog>
  )
}
