async function importAttractions() {
  console.log(`開始匯入 ${attractions.length} 個景點...`)

  // 批次插入，每次 10 筆
  const batchSize = 10
  let successCount = 0
  let errorCount = 0

  for (let i = 0; i < attractions.length; i += batchSize) {
    const batch = attractions.slice(i, i + batchSize)

    const { data, error } = await supabase
      .from('attractions')
      .insert(batch)
      .select('id, name, city_id')

    if (error) {
      console.error(`批次 ${Math.floor(i / batchSize) + 1} 錯誤:`, error.message)
      errorCount += batch.length
    } else {
      console.log(`批次 ${Math.floor(i / batchSize) + 1} 成功:`, data.map(d => `${d.name} (${d.city_id})`).join(', '))
      successCount += data.length
    }
  }

  console.log(`\n匯入完成！成功: ${successCount}, 失敗: ${errorCount}`)
}

