// 更新函數
async function updateAttractions(attractions, region) {
  console.log(`\n🔄 正在更新 ${region} 景點深度資料...`);
  let successCount = 0;

  for (const attraction of attractions) {
    const updateData = {
      duration_minutes: attraction.duration_minutes,
      opening_hours: attraction.opening_hours,
      tags: attraction.tags,
      notes: `${attraction.notes}\n\n旅遊提示：${attraction.tips}`
    };

    const { error } = await supabase
      .from('attractions')
      .update(updateData)
      .eq('country_id', 'korea')
      .ilike('name', attraction.name);

    if (error) {
      console.log(`  ❌ ${attraction.name}: ${error.message}`);
    } else {
      console.log(`  ✅ ${attraction.name}`);
      successCount++;
    }
  }

  return successCount;
}

